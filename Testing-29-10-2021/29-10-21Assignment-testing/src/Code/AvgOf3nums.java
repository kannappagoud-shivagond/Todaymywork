package Code;

import java.util.Scanner;

public class AvgOf3nums {

	public static void main(String[] args) {
		Scanner sc=new Scanner(System.in);
		System.out.println( "Enter three numbers" );
		int n1=sc.nextInt();
		int n2=sc.nextInt();
		int n3=sc.nextInt();
		System.out.println("Average="+((n1+n2+n3)/3));
		

	}

}
